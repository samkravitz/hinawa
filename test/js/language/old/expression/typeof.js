// Numbers
print(typeof 3);
print(typeof 3.14);
print(typeof (4));

// Strings
print(typeof "");
print(typeof 'foo');
print(typeof typeof 1);

// Undefined/null
print(typeof undefined);
print(typeof null);

// Objects
print(typeof { foo: "bar" });
print(typeof [1, 2, 3]);

// Functions
print(typeof function () { });
