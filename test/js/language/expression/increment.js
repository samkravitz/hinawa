var post_increment = 1;
print(post_increment++);
print(post_increment);

var pre_increment = 5;
print(++pre_increment);
print(pre_increment);

var post_decrement = 0;
print(post_decrement--);
print(post_decrement);

var pre_decrement = 100;
print(--pre_decrement);
print(pre_decrement);

// use in loops
for (var i = 0; i < 5; i++) {
  print(i);
}
